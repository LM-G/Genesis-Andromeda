angular
  .module('genesis.services.auth')
  .factory('User', User);

User.$inject = [];

function User() {
  /***********************************************************************************************/
  /* Variables                                                                                   */
  /***********************************************************************************************/
  var initValues = {
    isLogged: false,
    name: 'Visitor',
    role: -1
  };

  var user = {};

  /***********************************************************************************************/
  /* API publique                                                                                */
  /***********************************************************************************************/
  user.update = function(values) {
    angular.merge(user, initValues, values);
  };

  return user;
  /***********************************************************************************************/
  /* API interne                                                                                 */
  /***********************************************************************************************/
}